document.addEventListener("keydown", function(event) {

    // натиснута кнопка W
        if (event.keyCode === 87 || event.keyCode === 38){
            up()
            move_select_m()
        }
        // натиснута кнопка S
        if (event.keyCode === 83 || event.keyCode === 40){
            down()
            move_select_p()
        }

        // A або стрілка вліво
        if (event.keyCode === 65 || event.keyCode === 37){
            left()
        }

        // D або стрілка вправо
        if (event.keyCode === 68 || event.keyCode === 39){
            right()
}

    // натиснута кнопка E
    if (event.keyCode === 69) {
         right_select(event)
        }
    // натиснута кнопка F
    if (event.keyCode === 70) {
        center()
        }
    // натиснута кнопка Q
    if (event.keyCode === 81) {
        left_select(event)
    }
});


